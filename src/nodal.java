    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTextField1.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Enter Nodal ID");
        else {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms_project", "root", "root");
                PreparedStatement ps = con.prepareStatement("Select * FROM ginxpolling WHERE n_id=? ");
                ps.setString(1, n_id);
                ResultSet rs = ps.executeQuery();
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                while (rs.next()) {
                    Object o[] = {rs.getInt("p_id"), rs.getString("address")};
                    model.addRow(o);
                }  
            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

