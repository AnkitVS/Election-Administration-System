    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (!jTextField2.getText().isEmpty()) {
                p_id = jTextField2.getText();
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbms_project", "root", "root");
                PreparedStatement ps = con.prepareStatement("Select * FROM ginxpolling WHERE p_id=? ");
                ps.setString(1, p_id);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    this.setVisible(false);
                    new editPol(e_id,p_id, n_id).setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Polling ID does'n exist");
                }  
            } else {
                JOptionPane.showMessageDialog(null, "Enter Polling ID");
            }
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

